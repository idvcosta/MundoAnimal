package com.ingrid.mundoanimal.model;

public class Product {
    private int id;
    private String name;
    private String price;
    private String image;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPrice() {
        return price;
    }

    public String getImage() {
        return image;
    }
}
